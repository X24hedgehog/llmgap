import argparse
import ast
import gc
import json
import re
from dataclasses import dataclass
from pathlib import Path
from typing import Any, Dict, Iterable, List, Optional, Tuple

import pandas as pd
import torch
from transformers import AutoModelForCausalLM, AutoTokenizer


DEFAULT_GENERATOR_SPECS = [
    ("qwen05", "Qwen/Qwen2.5-0.5B-Instruct"),
    ("qwen15", "Qwen/Qwen2.5-1.5B-Instruct"),
    ("qwen3", "Qwen/Qwen2.5-3B-Instruct"),
]
DEFAULT_JUDGE_MODEL = "Qwen/Qwen2.5-7B-Instruct"


@dataclass
class ProblemRecord:
    question_id: str
    problem: str
    answer: Any
    answer_nl: str
    gold_label_subquestions: List[str]
    gold_label_subquestion_tree: Any


class HuggingFaceQwenModel:
    def __init__(
        self,
        model_name: str,
        dtype: Optional[torch.dtype],
        max_new_tokens: int,
        temperature: float,
        top_p: float,
    ):
        self.model_name = model_name
        self.dtype = dtype
        self.max_new_tokens = max_new_tokens
        self.temperature = temperature
        self.top_p = top_p
        self.model = None
        self.tokenizer = None

    def load(self) -> None:
        self.tokenizer = AutoTokenizer.from_pretrained(self.model_name, trust_remote_code=True)

        model_kwargs: Dict[str, Any] = {
            "trust_remote_code": True,
            "device_map": "auto",
        }
        if self.dtype is not None:
            model_kwargs["dtype"] = self.dtype

        self.model = AutoModelForCausalLM.from_pretrained(self.model_name, **model_kwargs)
        self.model.eval()

    def unload(self) -> None:
        if self.model is not None:
            del self.model
            self.model = None
        if self.tokenizer is not None:
            del self.tokenizer
            self.tokenizer = None
        gc.collect()
        if torch.cuda.is_available():
            torch.cuda.empty_cache()

    def generate_text(self, prompt_text: str) -> str:
        # Input: a single prompt string.
        # Output: the decoded text generated by the loaded Qwen model.
        if self.model is None or self.tokenizer is None:
            raise RuntimeError(f"Model '{self.model_name}' is not loaded")

        if hasattr(self.tokenizer, "apply_chat_template"):
            prompt_text = self.tokenizer.apply_chat_template(
                [{"role": "user", "content": prompt_text}],
                tokenize=False,
                add_generation_prompt=True,
            )

        inputs = self.tokenizer(prompt_text, return_tensors="pt")
        inputs = {key: value.to(self.model.device) for key, value in inputs.items()}

        do_sample = self.temperature > 0.0
        generation_kwargs: Dict[str, Any] = {
            **inputs,
            "max_new_tokens": self.max_new_tokens,
            "do_sample": do_sample,
            "pad_token_id": self.tokenizer.eos_token_id,
        }
        if do_sample:
            generation_kwargs["temperature"] = self.temperature
            generation_kwargs["top_p"] = self.top_p

        with torch.no_grad():
            output_ids = self.model.generate(**generation_kwargs)

        generated_ids = output_ids[0][inputs["input_ids"].shape[1]:]
        return self.tokenizer.decode(generated_ids, skip_special_tokens=True).strip()


class SubquestionGenerator:
    def __init__(self, model: HuggingFaceQwenModel):
        self.model = model

    def _build_prompt(self, problem: str) -> str:
        return (
            "You are helping solve arithmetic word problems by decomposition.\n"
            "Generate the minimal set of intermediate subquestions needed to solve the problem.\n\n"
            "Rules:\n"
            "1) Return ONLY valid JSON.\n"
            "2) JSON must have exactly this schema: {\"subquestions\": [str, ...]}\n"
            "3) Each subquestion must ask for a concrete quantity or fact needed for solving the problem.\n"
            "4) Subquestions must be minimal, factual, and non-redundant.\n"
            "5) Do not add explanations outside the JSON.\n"
            "6) Do not restate the final answer unless it is itself a necessary subquestion.\n\n"
            f"Problem:\n{problem}\n"
        )

    def generate(self, problem: str) -> List[str]:
        # Input: one math word-problem string.
        # Output: a list of generated subquestion strings.
        raw_text = self.model.generate_text(self._build_prompt(problem))
        return _parse_subquestions_json(raw_text)


class SubquestionJudge:
    def __init__(self, model: HuggingFaceQwenModel):
        self.model = model

    def _build_prompt(self, problem: str, gold_subquestions: List[str], candidate_subquestion: str) -> str:
        return (
            "You are a strict evaluator of semantic equivalence for math-problem subquestions.\n"
            "A candidate subquestion exists in the gold set if it has the same meaning, even if worded differently.\n"
            "Example: 'How many apples Alice has?' and 'What is the number of apples Alice holds?' should match.\n\n"
            "Return ONLY valid JSON with exactly this schema:\n"
            "{\"exists_in_gold\": bool, \"matched_gold_subquestion\": str|null, \"reason\": str}\n\n"
            "Decision rules:\n"
            "1) Match by meaning, not exact wording.\n"
            "2) If the candidate is broader, narrower, or asks a different quantity or relation, mark false.\n"
            "3) If it matches, return the single best matching gold subquestion string verbatim.\n"
            "4) Keep reason short.\n\n"
            f"Problem:\n{problem}\n\n"
            f"Gold subquestions:\n{json.dumps(gold_subquestions, ensure_ascii=True)}\n\n"
            f"Candidate subquestion:\n{candidate_subquestion}\n"
        )

    def judge_one(self, problem: str, gold_subquestions: List[str], candidate_subquestion: str) -> Dict[str, Any]:
        # Input: one problem string, the gold subquestion list, and one generated candidate subquestion.
        # Output: a dict with keys candidate_subquestion, exists_in_gold, matched_gold_subquestion, reason.
        raw_text = self.model.generate_text(
            self._build_prompt(problem, gold_subquestions, candidate_subquestion)
        )
        parsed = _parse_json_object(raw_text)

        exists_in_gold = bool(parsed.get("exists_in_gold", False))
        matched_gold = parsed.get("matched_gold_subquestion")
        reason = str(parsed.get("reason", ""))

        if exists_in_gold and matched_gold is None:
            matched_gold = ""

        return {
            "candidate_subquestion": candidate_subquestion,
            "exists_in_gold": exists_in_gold,
            "matched_gold_subquestion": matched_gold,
            "reason": reason,
        }


class MetricScorer:
    @staticmethod
    def normalize_text(text: str) -> str:
        cleaned = re.sub(r"\s+", " ", text.strip().lower())
        cleaned = re.sub(r"[^a-z0-9 ?]", "", cleaned)
        return cleaned

    @classmethod
    def lexical_jaccard_similarity(cls, gold: Iterable[str], generated: Iterable[str]) -> float:
        gold_set = {cls.normalize_text(x) for x in gold if str(x).strip()}
        gen_set = {cls.normalize_text(x) for x in generated if str(x).strip()}
        if not gold_set and not gen_set:
            return 1.0
        if not gold_set or not gen_set:
            return 0.0
        return len(gold_set & gen_set) / len(gold_set | gen_set)

    @classmethod
    def score_with_judge(
        cls,
        gold_subquestions: List[str],
        generated_subquestions: List[str],
        judge_results: List[Dict[str, Any]],
    ) -> Dict[str, Any]:
        # Input: gold subquestions, generated subquestions, and per-subquestion judge decisions.
        # Output: a dict of aggregate metrics such as semantic_jaccard, lexical_jaccard, precision, recall, and f1.
        gold_norm = {cls.normalize_text(x) for x in gold_subquestions if str(x).strip()}
        generated_unique_norm = {cls.normalize_text(x) for x in generated_subquestions if str(x).strip()}

        matched_gold_norm = set()
        matched_generated_norm = set()
        for row in judge_results:
            if row.get("exists_in_gold"):
                candidate = row.get("candidate_subquestion")
                if isinstance(candidate, str) and candidate.strip():
                    matched_generated_norm.add(cls.normalize_text(candidate))
                matched_gold = row.get("matched_gold_subquestion")
                if isinstance(matched_gold, str) and matched_gold.strip():
                    matched_gold_norm.add(cls.normalize_text(matched_gold))

        pred_count = len(generated_unique_norm)
        gold_count = len(gold_norm)
        matched_generated_count = len(matched_generated_norm)
        matched_gold_count = len(matched_gold_norm)

        precision = matched_generated_count / pred_count if pred_count else 0.0
        recall = matched_gold_count / gold_count if gold_count else 0.0
        f1 = (2 * precision * recall / (precision + recall)) if (precision + recall) else 0.0
        semantic_jaccard_den = pred_count + gold_count - matched_gold_count
        semantic_jaccard = matched_gold_count / semantic_jaccard_den if semantic_jaccard_den else 1.0

        return {
            "semantic_jaccard": semantic_jaccard,
            "lexical_jaccard": cls.lexical_jaccard_similarity(gold_subquestions, generated_subquestions),
            "precision": precision,
            "recall": recall,
            "f1": f1,
            "matched_generated_count": matched_generated_count,
            "matched_gold_count": matched_gold_count,
            "gold_count": gold_count,
            "generated_count": pred_count,
        }


class SubquestionEvaluationPipeline:
    REQUIRED_COLUMNS = {
        "problem",
        "answer",
        "answer_nl",
        "subquestions",
        "subquestion_tree",
    }

    def __init__(self, input_files: List[Path]):
        self.input_files = input_files

    def load_records(self, limit: Optional[int] = None) -> List[ProblemRecord]:
        # Input: optional maximum number of rows to load across all input CSV files.
        # Output: a list of ProblemRecord objects with parsed gold subquestions and trees.
        records: List[ProblemRecord] = []

        for file_path in self.input_files:
            df = pd.read_csv(file_path)
            missing = self.REQUIRED_COLUMNS - set(df.columns)
            if missing:
                raise ValueError(f"Missing columns in {file_path}: {sorted(missing)}")

            for idx, row in df.iterrows():
                records.append(
                    ProblemRecord(
                        question_id=f"{file_path.stem}__{idx}",
                        problem=str(row.get("problem", "")),
                        answer=row.get("answer"),
                        answer_nl=str(row.get("answer_nl", "")),
                        gold_label_subquestions=_coerce_list_field(row.get("subquestions")),
                        gold_label_subquestion_tree=_coerce_obj_field(row.get("subquestion_tree")),
                    )
                )

                if limit is not None and len(records) >= limit:
                    return records

        return records

    def initialize_rows(self, records: List[ProblemRecord]) -> pd.DataFrame:
        # Input: a list of ProblemRecord objects.
        # Output: a base DataFrame with one row per problem and only gold/reference fields populated.
        return pd.DataFrame(
            [
                {
                    "question_id": record.question_id,
                    "problem": record.problem,
                    "answer": record.answer,
                    "answer_nl": record.answer_nl,
                    "gold_label_subquestions": json.dumps(record.gold_label_subquestions, ensure_ascii=True),
                    "gold_label_subquestion_tree": json.dumps(record.gold_label_subquestion_tree, ensure_ascii=True),
                }
                for record in records
            ]
        )

    def add_generated_subquestions(
        self,
        scored_df: pd.DataFrame,
        records: List[ProblemRecord],
        model_alias: str,
        generator: SubquestionGenerator,
    ) -> None:
        # Input: the output DataFrame, the loaded problem records, a model alias, and a generator.
        # Output: mutates scored_df by adding one generated_subquestions__<alias> column of JSON lists.
        scored_df[f"generated_subquestions__{model_alias}"] = [
            json.dumps(generator.generate(record.problem), ensure_ascii=True) for record in records
        ]

    def judge_generated_subquestions(
        self,
        scored_df: pd.DataFrame,
        records: List[ProblemRecord],
        generator_alias: str,
        judge: SubquestionJudge,
    ) -> None:
        # Input: the partially filled DataFrame, the problem records, one generator alias, and a judge model.
        # Output: mutates scored_df by adding judge details plus scalar metric columns for that generator alias.
        matched_column = f"matched_gold_subquestions__{generator_alias}"
        score_column = f"scores__{generator_alias}"
        judge_column = f"judge_details__{generator_alias}"
        generated_column = f"generated_subquestions__{generator_alias}"
        scalar_score_column = f"score__{generator_alias}"
        semantic_jaccard_column = f"semantic_jaccard__{generator_alias}"
        lexical_jaccard_column = f"lexical_jaccard__{generator_alias}"
        precision_column = f"precision__{generator_alias}"
        recall_column = f"recall__{generator_alias}"
        f1_column = f"f1__{generator_alias}"

        matched_values: List[str] = []
        score_values: List[str] = []
        judge_values: List[str] = []
        scalar_score_values: List[float] = []
        semantic_jaccard_values: List[float] = []
        lexical_jaccard_values: List[float] = []
        precision_values: List[float] = []
        recall_values: List[float] = []
        f1_values: List[float] = []

        for row, record in zip(scored_df.itertuples(index=False), records):
            generated_subqs = _coerce_list_field(getattr(row, generated_column))
            judge_results = [
                judge.judge_one(record.problem, record.gold_label_subquestions, candidate)
                for candidate in generated_subqs
            ]
            matched_gold_subqs = [
                result["matched_gold_subquestion"]
                for result in judge_results
                if result.get("exists_in_gold") and isinstance(result.get("matched_gold_subquestion"), str)
            ]
            score = MetricScorer.score_with_judge(
                gold_subquestions=record.gold_label_subquestions,
                generated_subquestions=generated_subqs,
                judge_results=judge_results,
            )

            matched_values.append(json.dumps(matched_gold_subqs, ensure_ascii=True))
            score_values.append(json.dumps(score, ensure_ascii=True))
            judge_values.append(json.dumps(judge_results, ensure_ascii=True))
            scalar_score_values.append(float(score["semantic_jaccard"]))
            semantic_jaccard_values.append(float(score["semantic_jaccard"]))
            lexical_jaccard_values.append(float(score["lexical_jaccard"]))
            precision_values.append(float(score["precision"]))
            recall_values.append(float(score["recall"]))
            f1_values.append(float(score["f1"]))

        scored_df[matched_column] = matched_values
        scored_df[score_column] = score_values
        scored_df[judge_column] = judge_values
        scored_df[scalar_score_column] = scalar_score_values
        scored_df[semantic_jaccard_column] = semantic_jaccard_values
        scored_df[lexical_jaccard_column] = lexical_jaccard_values
        scored_df[precision_column] = precision_values
        scored_df[recall_column] = recall_values
        scored_df[f1_column] = f1_values


def get_torch_dtype(dtype_str: str) -> Optional[torch.dtype]:
    # Input: one of auto, bfloat16, float16, float32.
    # Output: the corresponding torch dtype, or None for auto.
    if dtype_str == "auto":
        return None
    if dtype_str == "bfloat16":
        return torch.bfloat16
    if dtype_str == "float16":
        return torch.float16
    if dtype_str == "float32":
        return torch.float32
    raise ValueError(f"Unsupported dtype: {dtype_str}")


def _coerce_list_field(value: Any) -> List[str]:
    # Input: a CSV cell value that may already be a list, a Python-literal string list, or plain text.
    # Output: a normalized Python list of strings.
    if isinstance(value, list):
        return [str(v) for v in value]
    if isinstance(value, str):
        text = value.strip()
        if not text:
            return []
        try:
            parsed = ast.literal_eval(text)
            if isinstance(parsed, list):
                return [str(v) for v in parsed]
        except (SyntaxError, ValueError):
            pass
        return [line.strip(" -\t") for line in text.splitlines() if line.strip()]
    return []


def _coerce_obj_field(value: Any) -> Any:
    # Input: a CSV cell value that may represent a dict/list object or serialized object text.
    # Output: a parsed Python object when possible, otherwise a fallback dict with raw content.
    if isinstance(value, (dict, list)):
        return value
    if isinstance(value, str):
        text = value.strip()
        if not text:
            return {}
        try:
            return ast.literal_eval(text)
        except (SyntaxError, ValueError):
            return {"raw": text}
    return {"raw": value}


def _parse_model_specs(specs: Optional[List[str]]) -> List[Tuple[str, str]]:
    # Input: CLI values like ["qwen05=Qwen/Qwen2.5-0.5B-Instruct", ...] or None.
    # Output: a list of (alias, model_name) tuples, using defaults when specs is empty.
    if not specs:
        return DEFAULT_GENERATOR_SPECS.copy()

    pairs: List[Tuple[str, str]] = []
    for spec in specs:
        if "=" not in spec:
            raise ValueError(f"Invalid --model spec '{spec}'. Expected alias=model_name")
        alias, model_name = spec.split("=", 1)
        alias = alias.strip()
        model_name = model_name.strip()
        if not alias or not model_name:
            raise ValueError(f"Invalid --model spec '{spec}'. Expected alias=model_name")
        pairs.append((alias, model_name))
    return pairs


def _parse_json_object(text: str) -> Dict[str, Any]:
    # Input: free-form model output text that may contain a JSON object.
    # Output: the parsed JSON dict, or {} if parsing fails.
    try:
        parsed = json.loads(text)
        if isinstance(parsed, dict):
            return parsed
    except json.JSONDecodeError:
        pass

    match = re.search(r"\{.*\}", text, flags=re.DOTALL)
    if match:
        try:
            parsed = json.loads(match.group(0))
            if isinstance(parsed, dict):
                return parsed
        except json.JSONDecodeError:
            pass

    return {}


def _parse_subquestions_json(text: str) -> List[str]:
    # Input: model output text expected to contain {"subquestions": [...]}.
    # Output: a list of subquestion strings, with a line-based fallback if JSON parsing fails.
    parsed = _parse_json_object(text)
    value = parsed.get("subquestions", []) if isinstance(parsed, dict) else []

    if isinstance(value, list):
        return [str(v).strip() for v in value if str(v).strip()]

    lines = [line.strip(" -\t") for line in text.splitlines() if line.strip()]
    return [line for line in lines if "{" not in line and "}" not in line]


def main() -> None:
    parser = argparse.ArgumentParser(
        description=(
            "Pipeline: Qwen 0.5B/1.5B/3B generate subquestions, Qwen 7B judges semantic matches "
            "against gold subquestions, then the script computes scores and saves a CSV."
        )
    )
    parser.add_argument(
        "--input-glob",
        type=str,
        default="out/*_100.csv",
        help="Glob pattern to input CSV files generated by generate.py",
    )
    parser.add_argument(
        "--output-path",
        type=str,
        default="out/subquestion_eval_scores.csv",
        help="Path to save scored output CSV",
    )
    parser.add_argument(
        "--model",
        action="append",
        help="Generator model spec in form alias=model_name. Repeat for multiple models.",
    )
    parser.add_argument(
        "--judge-model",
        type=str,
        default=DEFAULT_JUDGE_MODEL,
        help="Judge model name. Default: Qwen/Qwen2.5-7B-Instruct",
    )
    parser.add_argument(
        "--dtype",
        type=str,
        default="auto",
        choices=["auto", "bfloat16", "float16", "float32"],
        help="Torch dtype for all Hugging Face Qwen models.",
    )
    parser.add_argument(
        "--generator-max-new-tokens",
        type=int,
        default=256,
        help="Maximum number of new tokens for generator models.",
    )
    parser.add_argument(
        "--judge-max-new-tokens",
        type=int,
        default=128,
        help="Maximum number of new tokens for the judge model.",
    )
    parser.add_argument(
        "--temperature",
        type=float,
        default=0.0,
        help="Generation temperature for generator models.",
    )
    parser.add_argument(
        "--top-p",
        type=float,
        default=1.0,
        help="Top-p sampling value for generator models.",
    )
    parser.add_argument(
        "--judge-temperature",
        type=float,
        default=0.0,
        help="Generation temperature for judge model.",
    )
    parser.add_argument(
        "--judge-top-p",
        type=float,
        default=1.0,
        help="Top-p sampling value for judge model.",
    )
    parser.add_argument(
        "--limit",
        type=int,
        default=None,
        help="Optional max number of problems to evaluate.",
    )

    args = parser.parse_args()

    input_files = sorted(Path(".").glob(args.input_glob))
    if not input_files:
        raise FileNotFoundError(f"No input files found for pattern: {args.input_glob}")

    model_specs = _parse_model_specs(args.model)
    dtype = get_torch_dtype(args.dtype)

    pipeline = SubquestionEvaluationPipeline(input_files=input_files)
    records = pipeline.load_records(limit=args.limit)
    scored_df = pipeline.initialize_rows(records)

    for alias, model_name in model_specs:
        generator_model = HuggingFaceQwenModel(
            model_name=model_name,
            dtype=dtype,
            max_new_tokens=args.generator_max_new_tokens,
            temperature=args.temperature,
            top_p=args.top_p,
        )
        try:
            generator_model.load()
            pipeline.add_generated_subquestions(
                scored_df=scored_df,
                records=records,
                model_alias=alias,
                generator=SubquestionGenerator(generator_model),
            )
        finally:
            generator_model.unload()

    judge_model = HuggingFaceQwenModel(
        model_name=args.judge_model,
        dtype=dtype,
        max_new_tokens=args.judge_max_new_tokens,
        temperature=args.judge_temperature,
        top_p=args.judge_top_p,
    )

    try:
        judge_model.load()
        judge = SubquestionJudge(judge_model)
        for alias, _ in model_specs:
            pipeline.judge_generated_subquestions(
                scored_df=scored_df,
                records=records,
                generator_alias=alias,
                judge=judge,
            )
    finally:
        judge_model.unload()

    output_path = Path(args.output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    scored_df.to_csv(output_path, index=False)

    print(f"Saved {len(scored_df)} scored rows to: {output_path}")


if __name__ == "__main__":
    main()
